module menu (
    input wire [5:0] x, // 0-31 (32 columns)
    input wire [3:0] y, // 0-11 (12 rows)
    output reg menu_on
);

    reg [39:0] row_data;

    always @(*) begin
        case(y)
            // Format: [1 pad]_[M]_[2 space]_[E]_[2 space]_[N]_[2 space]_[U]_[1 pad]
            4'd0:  row_data = 40'b0_11000011_00_11111111_00_11000011_00_11000011_0;
            4'd1:  row_data = 40'b0_11100111_00_11111111_00_11100011_00_11000011_0;
            4'd2:  row_data = 40'b0_11111111_00_11000000_00_11110011_00_11000011_0;
            4'd3:  row_data = 40'b0_11011011_00_11000000_00_11111011_00_11000011_0;
            4'd4:  row_data = 40'b0_11000011_00_11111100_00_11011111_00_11000011_0;
            4'd5:  row_data = 40'b0_11000011_00_11111100_00_11001111_00_11000011_0;
            4'd6:  row_data = 40'b0_11000011_00_11000000_00_11000111_00_11000011_0;
            4'd7:  row_data = 40'b0_11000011_00_11000000_00_11000011_00_11000011_0;
            4'd8:  row_data = 40'b0_11000011_00_11111111_00_11000011_00_01111110_0;
            4'd9:  row_data = 40'b0_11000011_00_11111111_00_11000011_00_00111100_0;
            4'd10: row_data = 40'b0_10000001_00_10000001_00_10000001_00_00000000_0;
            4'd11: row_data = 40'b0_10000001_00_10000001_00_10000001_00_00000000_0;
            default: row_data = 40'b0; 
        endcase
        
        // Use 39 instead of 31 for the new width
        menu_on = row_data[6'd39 - x];
    end
endmodule