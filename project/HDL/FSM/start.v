module start (
    input wire [5:0] x, // 0-47 (6 bits wide)
    input wire [3:0] y, // 0-11
    output reg pixel_on
);
    reg [47:0] row_data;
    always @(*) begin
        case(y)
            // Format: [S] _00_ [T] _00_ [A] _00_ [R] _00_ [T]
            4'd0:  row_data = 48'b01111110_00_11111111_00_00111100_00_11111110_00_11111111;
            4'd1:  row_data = 48'b11111111_00_11111111_00_01111110_00_11111111_00_11111111;
            4'd2:  row_data = 48'b11000000_00_00011000_00_11000011_00_11000011_00_00011000;
            4'd3:  row_data = 48'b11000000_00_00011000_00_11000011_00_11000011_00_00011000;
            4'd4:  row_data = 48'b01111110_00_00011000_00_11111111_00_11111111_00_00011000;
            4'd5:  row_data = 48'b00111111_00_00011000_00_11111111_00_11111110_00_00011000;
            4'd6:  row_data = 48'b00000011_00_00011000_00_11000011_00_11000110_00_00011000;
            4'd7:  row_data = 48'b00000011_00_00011000_00_11000011_00_11000011_00_00011000;
            4'd8:  row_data = 48'b11111111_00_00011000_00_11000011_00_11000011_00_00011000;
            4'd9:  row_data = 48'b01111110_00_00011000_00_11000011_00_11000011_00_00011000;
            4'd10: row_data = 48'b10000000_00_10000001_00_10000001_00_10000001_00_10000001; // Base spikes
            4'd11: row_data = 48'b10000000_00_10000001_00_10000001_00_10000001_00_10000001;
            default: row_data = 48'b0;
        endcase
        pixel_on = row_data[6'd47 - x];
    end
endmodule